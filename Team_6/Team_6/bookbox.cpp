#define COLLISION_SIZE	(D3DXVECTOR3(80.0f,400.0f,40.0f))	// サイズ
#define COLLISION_SIZE2	(D3DXVECTOR3(40.0f,400.0f,80.0f))	// サイズ
#define ROT_90			(D3DXToRadian(89.0f))				// 向き
